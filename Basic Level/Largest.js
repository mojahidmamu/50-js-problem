// Find the Largest Number in an Array
function checkLargeNumber(num){
  return Math.max(...num)
}
console.log(checkLargeNumber([3,10,5,6,89,7]))
// Find the smaller number
function smallerNumber(number){
  return Math.min(...number)
}
console.log(smallerNumber([4,5,6,9,8,7,10]))
